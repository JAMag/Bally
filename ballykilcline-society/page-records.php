        <h2 class="">History</h2>
          <p class="">The Ballykilcline townlands, like so many others, had been seized by the British government from the Irish citizenry who had ancestral rights to the lands. It's residents, living on the brink of starvation on over-tilled farms, were forced to pay rents to the English Crown through the landlord, Lord Hartland. The townlands supported a population of just over 500 and was subdivided into 463 minute holdings or subdivisions occupied by these "cottier labourers". A land survey completed in 1836 shows the location of each farm and the number of acres held by the individual families. Families who were evicted and who sailed on "coffin" ships are listed below. However, many residents died during the worst famine years. They are buried in an old cemetery located on the property, some individually, some in a mass grave. Family members that stayed behind and moved off the property are difficult to trace. Some eventually returned and began the process of rebuilding what had been lost and you can find their descendants there today. </p>
